% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%Enter the Generator Matrix
g=str2num(get(handles.edit4,'string'))
[a,b]=size(g);
c=b-a;
h=gen2par(g) 
ht=transpose(h)
%Enter the received code word
v=str2num(get(handles.edit9,'string'))
syndromevector=v*ht
%Syndrome of a Given Code vector
set(handles.edit10,'string',mat2str(syndromevector))
%Syndrome of a Given Code vector
s=mod(syndromevector,2)
set(handles.edit11,'string',mat2str(s))

if sum(s)==0
    ss='No error'
    set(handles.edit12,'string',ss)
    for i=1:length(v)
        u=v(c+1:b) 
        set(handles.edit7,'string',mat2str(u))
        break;
    end 
else
    for i=1:size(ht,1)
        if (ht(i,1:c)==s)
            v(i)=1-v(i)
            break;
        end
    end
    set(handles.edit12,'string',i)
    
    for i=1:length(v)
        u=v(c+1:b)
        set(handles.edit7,'string',mat2str(u))
        break;
     end 
end

